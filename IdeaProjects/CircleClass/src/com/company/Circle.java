package com.company;

public class Circle {
    //instance variables
    public double radius, x, y;

    //Constructors
    public Circle(){
        radius = 0;
        x = 0;
        y = 0;
    }

    public Circle (double r, double xcor, double ycor){
        radius = r;
        x = xcor;
        y = ycor;
    }
}
